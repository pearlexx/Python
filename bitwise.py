# ---------- Binary AND ----------

a = 50      # 110010
b = 25      # 011001
c = a & b   # 010000

print(c)

# ---------- Binary RIGHT SHIFT ----------

x = 240     # 11110000
y = x >> 2  # 00111100
z = x << 2  # 1111000000
i = x >> 4  # 1111
j = x >> 6  # 0011
k = x >> 1  # 01111000

print(y)
print(z)
print(i)
print(j)
print(k)
